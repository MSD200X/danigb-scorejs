var t = require('../test')
var value = require('../../lib/duration/value')

t.describe('duration/name', {
  'duration value from name': function () {
    t.equal(value('long'), 4)
    t.equal(value('double'), 2)
    t.equal(value('whole'), 1)
    t.equal(value('half'), 1 / 2)
    t.equal(value('quarter'), 1 / 4)
    t.equal(value('eighth'), 1 / 8)
    t.equal(value('sixteenth'), 1 / 16)
    t.equal(value('thirty-second'), 1 / 32)
  },
  'duration value from letter': function () {
    t.equal(value('l'), 4)
    t.equal(value('l.'), 4 + 2)
    t.equal(value('l..'), 4 + 2 + 1)
    t.equal(value('d'), 2)
    t.equal(value('d.'), 2 + 1)
    t.equal(value('d..'), 2 + 1 + 1 / 2)
    t.equal(value('w'), 1)
    t.equal(value('w.'), 1 + 1 / 2)
    t.equal(value('w..'), 1 + 1 / 2 + 1 / 4)
    t.equal(value('h'), 1 / 2)
    t.equal(value('h.'), 1 / 2 + 1 / 4)
    t.equal(value('h..'), 1 / 2 + 1 / 4 + 1 / 8)
    t.equal(value('q'), 1 / 4)
    t.equal(value('q.'), 1 / 4 + 1 / 8)
    t.equal(value('q..'), 1 / 4 + 1 / 8 + 1 / 16)
    t.equal(value('e'), 1 / 8)
    t.equal(value('e.'), 1 / 8 + 1 / 16)
    t.equal(value('e..'), 1 / 8 + 1 / 16 + 1 / 32)
    t.equal(value('s'), 1 / 16)
    t.equal(value('s.'), 1 / 16 + 1 / 32)
    t.equal(value('s..'), 1 / 16 + 1 / 32 + 1 / 64)
  },
  'duration value from number': function () {
    t.equal(value('0.25'), 4)
    t.equal(value('0.25.'), 4 + 2)
    t.equal(value('0.25..'), 4 + 2 + 1)
    t.equal(value('0.5'), 2)
    t.equal(value('0.5.'), 2 + 1)
    t.equal(value('0.5..'), 2 + 1 + 1 / 2)
    t.equal(value('1'), 1)
    t.equal(value('1.'), 1 + 1 / 2)
    t.equal(value('1..'), 1 + 1 / 2 + 1 / 4)
    t.equal(value('2'), 1 / 2)
    t.equal(value('2.'), 1 / 2 + 1 / 4)
    t.equal(value('2..'), 1 / 2 + 1 / 4 + 1 / 8)
    t.equal(value('4'), 1 / 4)
    t.equal(value('4.'), 1 / 4 + 1 / 8)
    t.equal(value('4..'), 1 / 4 + 1 / 8 + 1 / 16)
    t.equal(value('8'), 1 / 8)
    t.equal(value('8.'), 1 / 8 + 1 / 16)
    t.equal(value('8..'), 1 / 8 + 1 / 16 + 1 / 32)
    t.equal(value('16'), 1 / 16)
    t.equal(value('16.'), 1 / 16 + 1 / 32)
    t.equal(value('16..'), 1 / 16 + 1 / 32 + 1 / 64)
  },
  'duration value from triplets': function () {
    t.equal(value('qt'), (1 / 4 + 1 / 4) / 3)
  }
}).export(module)
